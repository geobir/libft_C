#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*save;

	save = s1;
	ft_strend(&save);
	while (*s2 != '\0')
	{
		*save = *s2;
		++save;
		++s2;
	}
	*save = '\0';
	return (s1);
}
