#include "libft.h"

char	*ft_itoa(int n)
{
	char	*s;
	int		i;

	i = ft_numlen(n);
	s = (char*)malloc(sizeof(char) * i + 1);
	if (n < 0)
	{
		*s ='-';
		s[i+1] = '\0';
		while(i > 0)
		{
			s[i--] = ft_numabs(n%10) + '0';
			n /= 10;
		}
	}else if (n)
	{
		s[i] = '\0';
		while(--i >= 0)
		{
			s[i] = n%10 + '0';
			n /= 10;
		}
	}
	else
	{
		s[0] = '0';
		s[1] = '\0';
	}
	return (s);
}
