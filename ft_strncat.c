#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*save;

	save = s1;
	ft_strend(&save);
	while (*s2 != '\0' && n != 0)
	{
		*save = *s2;
		--n;
		++save;
		++s2;
	}
	*save = '\0';
	return (s1);
}
